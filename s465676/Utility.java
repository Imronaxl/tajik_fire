package org.ITMO.s465676;

public class Utility {

    /**
     * Область соответствует обновленным требованиям:
     * - Q1 (x>=0, y>=0): треугольник с вершинами (0,0), (R,0), (0,R) - условие x + y <= R
     * - Q2 (x<=0, y>=0): прямоугольник от x=-R до x=0, от y=0 до y=R/2
     * - Q3 (x<=0, y<=0): четверть круга x^2+y^2 <= R^2
     * - Q4 (x>=0, y<=0): треугольник с вершинами (0,0), (R,0), (0,-R/2) - условие x - 2y <= R
     */
    public static boolean checkArea(double x, double y, double r) {
        // Нормализуем значения (убираем возможные погрешности)
        x = Math.round(x * 100000000.0) / 100000000.0;
        y = Math.round(y * 100000000.0) / 100000000.0;
        r = Math.round(r * 100000000.0) / 100000000.0;

        // Q1 (x >= 0, y >= 0): Треугольник с вершинами (0,0), (R,0), (0,R)
        if (x >= 0 && y >= 0) {
            return x >= 0 && y >= 0 && (x + y <= r);
        }

        // Q2 (x <= 0, y >= 0): Прямоугольник от x=-R до x=0, от y=0 до y=R/2
        if (x <= 0 && y >= 0) {
            return x <= 0 && y >= 0 && x >= -r && y <= r/2;
        }

        // Q3 (x <= 0, y <= 0): Четверть круга x² + y² ≤ R²
        if (x <= 0 && y <= 0) {
            return x <= 0 && y <= 0 && (x * x + y * y <= r * r);
        }

        // Q4 (x >= 0, y <= 0): Треугольник с вершинами (0,0), (R,0), (0,-R/2)
        if (x >= 0 && y <= 0) {
            return x >= 0 && y <= 0 && (x - 2 * y <= r);
        }

        return false;
    }
}


